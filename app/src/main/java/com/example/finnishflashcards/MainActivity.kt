@Composable
fun FlashCardThemeSelector(themes: Map<String, List<FlashCard>>) {
    var selectedTheme by remember { mutableStateOf<String?>(null) }

    if (selectedTheme == null) {
        // Theme picker UI
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(24.dp),
            horizontalAlignment = Alignment.CenterHorizontally,
            verticalArrangement = Arrangement.Center
        ) {
            Text("Finnish Flashcards", style = MaterialTheme.typography.headlineLarge)
            Spacer(modifier = Modifier.height(32.dp))
            Text("Choose a theme:", style = MaterialTheme.typography.titleMedium)
            Spacer(modifier = Modifier.height(16.dp))
            LazyColumn(horizontalAlignment = Alignment.CenterHorizontally) {
                items(themes.keys.size) { idx ->
                    val themeName = themes.keys.elementAt(idx)
                    Button(
                        onClick = { selectedTheme = themeName },
                        modifier = Modifier
                            .fillMaxWidth()
                            .padding(vertical = 6.dp)
                    ) {
                        Text(themeName)
                    }
                }
            }
        }
    } else {
        FlashCardApp(
            themeName = selectedTheme!!,
            flashcards = themes[selectedTheme]!!,
            onBackToThemes = { selectedTheme = null }
        )
    }
}

@Composable
fun FlashCardApp(
    themeName: String,
    flashcards: List<FlashCard>,
    onBackToThemes: () -> Unit
) {
    var shown by remember { mutableStateOf(mutableSetOf<Int>()) }
    var currentIdx by remember { mutableStateOf(-1) }
    var showEnglish by remember { mutableStateOf(false) }
    var correct by remember { mutableStateOf(0) }
    var wrong by remember { mutableStateOf(0) }
    var isFinished by remember { mutableStateOf(false) }

    fun nextCard() {
        if (shown.size == flashcards.size) {
            isFinished = true
            return
        }
        val available = flashcards.indices.filterNot { it in shown }
        if (available.isNotEmpty()) {
            currentIdx = available.random()
            showEnglish = false
        }
    }

    // Start or reset session when entering this Composable
    LaunchedEffect(themeName) {
        shown = mutableSetOf()
        correct = 0
        wrong = 0
        isFinished = false
        nextCard()
    }

    Column(
        modifier = Modifier
            .fillMaxSize()
            .padding(24.dp)
    ) {
        Row(
            modifier = Modifier.fillMaxWidth(),
            verticalAlignment = Alignment.CenterVertically
        ) {
            Button(onClick = onBackToThemes) {
                Text("Back")
            }
            Spacer(modifier = Modifier.weight(1f))
            Text(themeName, style = MaterialTheme.typography.titleLarge)
        }
        Spacer(modifier = Modifier.height(12.dp))
        Divider()
        Spacer(modifier = Modifier.height(24.dp))

        if (isFinished) {
            EndScreen(
                correct = correct,
                wrong = wrong,
                total = flashcards.size,
                onRestart = {
                    shown = mutableSetOf()
                    correct = 0
                    wrong = 0
                    isFinished = false
                    nextCard()
                }
            )
        } else if (currentIdx >= 0) {
            val card = flashcards[currentIdx]
            FlashCardScreen(
                card = card,
                showEnglish = showEnglish,
                progress = shown.size + 1,
                total = flashcards.size,
                onShowEnglish = { showEnglish = true },
                onMark = { knew ->
                    if (currentIdx !in shown) {
                        shown.add(currentIdx)
                        if (knew) correct++ else wrong++
                    }
                    nextCard()
                }
            )
        }
    }
}

